#Faça um Programa que peça as 4 notas bimestrais e mostre a média.

nota1 = int(input("Digite o primeiro numero: "))
nota2 = int(input("Digite o segundo numero: "))
nota3 = int(input("Digite o terceiro numero: "))
nota4 = int(input("Digite o quarto numero: "))

media = (nota1+nota2+nota3+nota4)/4

print(f"O resultado da media é: {media}")

